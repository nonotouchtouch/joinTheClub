package com.hanming.po;

public class MyApplicationLog {
	private String clubId;
	private String cName;
	private String item;
	private int cStar;
	private String date;
	private int state;
	public String getClubId() {
		return clubId;
	}
	public void setClubId(String clubId) {
		this.clubId = clubId;
	}
	public String getcName() {
		return cName;
	}
	public void setcName(String cName) {
		this.cName = cName;
	}

	public String getcType() {
		return item;
	}
	public void setcType(String item) {
		this.item = item;
	}
	public int getcStar() {
		return cStar;
	}
	public void setcStar(int cStar) {
		this.cStar = cStar;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public int getState() {
		return state;
	}
	public void setState(int state) {
		this.state = state;
	}
	

}
